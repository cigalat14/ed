using System;
using Gdk;

public class operaciones
{
    public double suma (double a, double b)
    {
        return a + b;
    }
    public double resta(double a, double b)
    {
        return a - b;
    }
    public double multiplicacion(double a, double b)
    {
        return a * b;
    }
    public double division(double a, double b)
    {
        return a / b;
    }

}